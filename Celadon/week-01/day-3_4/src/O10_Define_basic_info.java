public class O1O_Define_basic_info {
    public static void main(String[] args) {
        // Define several things as a variable then print their values
        // Your name as a string
        String name = "Jozef Michalicka";
        // Your age as an integer
        int age = 30;
        // Your height in meters as a double
        double  height = (1.77);
        // Whether you are married or not as a boolean
        boolean married = false;

        if (married == true)
            System.out.println("Hello, my name is " + name + ", I am " + age + " years old, " + height + " meters tall and am I married? Yes!");
        else
            System.out.println("Hello, my name is " + name + ", I am " + age + " years old, " + height + " meters tall and am I married? No!");


    }
}
